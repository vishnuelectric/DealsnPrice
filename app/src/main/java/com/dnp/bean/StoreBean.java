package com.dnp.bean;

public class StoreBean {
	public int coupon_total;
	public int deal_count;
	public String store_url;
	public String store_image;
	public String store_id;
	public String store_description;
	public int getCoupon_total() {
		return coupon_total;
	}
	public void setCoupon_total(int coupon_total) {
		this.coupon_total = coupon_total;
	}
	public int getDeal_count() {
		return deal_count;
	}
	public void setDeal_count(int deal_count) {
		this.deal_count = deal_count;
	}
	public String getStore_url() {
		return store_url;
	}
	public void setStore_url(String store_url) {
		this.store_url = store_url;
	}
	public String getStore_image() {
		return store_image;
	}
	public void setStore_image(String store_image) {
		this.store_image = store_image;
	}
	public String getStore_id() {
		return store_id;
	}
	public void setStore_id(String store_id) {
		this.store_id = store_id;
	}
	public String getStore_description() {
		return store_description;
	}
	public void setStore_description(String store_description) {
		this.store_description = store_description;
	}
}
