package com.dnp.bean;

public class AlertProductBean {
	public String product_id;
	public String alert_amount;
	public String alert_duration;
	public String getProduct_id() {
		return product_id;
	}
	public void setProduct_id(String product_id) {
		this.product_id = product_id;
	}
	public String getAlert_amount() {
		return alert_amount;
	}
	public void setAlert_amount(String alert_amount) {
		this.alert_amount = alert_amount;
	}
	public String getAlert_duration() {
		return alert_duration;
	}
	public void setAlert_duration(String alert_duration) {
		this.alert_duration = alert_duration;
	} 
}
